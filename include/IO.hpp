// -------------------------------------------------------------------------------------------------
#ifndef CSCI370FINALPROJECT_IO_H
#define CSCI370FINALPROJECT_IO_H

#include <iostream>
#include <unistd.h>
#include <termios.h>


// -------------------------------------------------------------------------------------------------
// ------------------------------------- STRUCT :: IO Utility --------------------------------------
// -------------------------------------------------------------------------------------------------
struct IO {

    // -----------------------------------------------------------------------------------------
    // ------------------------------- ATTRIBUTES :: Attributes --------------------------------
    // -----------------------------------------------------------------------------------------
    struct termios settings {};


    // -----------------------------------------------------------------------------------------
    // ------------------------------ CONSTRUCTOR :: Constructor -------------------------------
    // -----------------------------------------------------------------------------------------
    IO() { tcgetattr(STDIN_FILENO, &settings); }


    // -----------------------------------------------------------------------------------------
    // ------------------------------ UTILITY :: Read User Input -------------------------------
    // -----------------------------------------------------------------------------------------
    static void read(const std::string& output, std::string& input) {

        std::cout << output;
        std::getline(std::cin, input);

    }


    // -----------------------------------------------------------------------------------------
    // ------------------------------ UTILITY :: Read User Input -------------------------------
    // -----------------------------------------------------------------------------------------
    static std::string read(const std::string& output) {

        std::string   input;
        read(output, input);

        return input;

    }


    // -----------------------------------------------------------------------------------------
    // ------------------------------ UTILITY :: Show User Input -------------------------------
    // -----------------------------------------------------------------------------------------
    void show() {
        settings.c_lflag = (settings.c_lflag |  (ECHO));
        tcsetattr( STDIN_FILENO, TCSANOW, &settings );
    }


    // -----------------------------------------------------------------------------------------
    // ------------------------------ UTILITY :: Hide User Input -------------------------------
    // -----------------------------------------------------------------------------------------
    void hide() {
        settings.c_lflag = (settings.c_lflag & ~(ECHO));
        tcsetattr( STDIN_FILENO, TCSANOW, &settings );
    }


    // -----------------------------------------------------------------------------------------
    // ------------------------------ UTILITY :: Print Empty Line ------------------------------
    // -----------------------------------------------------------------------------------------
    static void line() {
        std::cout << std::endl;
    }


    // -----------------------------------------------------------------------------------------
    // ------------------------------ UTILITY :: Print to Output -------------------------------
    // -----------------------------------------------------------------------------------------
    static void print(const std::string& output) {
        std::cout << output << std::endl;
    }

};


#endif // CSCI370FINALPROJECT_IO_H
