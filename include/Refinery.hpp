// -------------------------------------------------------------------------------------------------
// ----------------------------------- HEADER :: Refinery Object -----------------------------------
// -------------------------------------------------------------------------------------------------
#ifndef CSCI370FINALPROJECT_REFINERY_HPP
#define CSCI370FINALPROJECT_REFINERY_HPP

#include <iomanip>
#include <sstream>

#include <occi.h>
using namespace oracle::occi;


// -------------------------------------------------------------------------------------------------
// ----------------------------------- STRUCT :: Refinery Object -----------------------------------
// -------------------------------------------------------------------------------------------------
struct Refinery {

    // -----------------------------------------------------------------------------------------
    // ------------------------------- ATTRIBUTES :: Attributes --------------------------------
    // -----------------------------------------------------------------------------------------
    std::string refining;
    std::string fid;


    // -----------------------------------------------------------------------------------------
    // ------------------------------ CONSTRUCTOR :: Constructor -------------------------------
    // -----------------------------------------------------------------------------------------
    Refinery(ResultSet* data) {

        refining = !data->isNull(2) ? data->getString(2) : "";
        fid = data->getString(1);

    }


    // -----------------------------------------------------------------------------------------
    // ------------------------ STRINGIFICATION :: Stringify Table Head ------------------------
    // -----------------------------------------------------------------------------------------
    static void stringify_head(std::stringstream& output) {

        output << std::endl;

        output << std::setw(11) << std::left << "Refinery ID" << "   ";
        output << std::setw(9)  << std::left << "Refining"    << "   ";

        output << std::endl;

        output << std::setfill('-');
        output << std::setw(11) << "" << "   ";
        output << std::setw(9)  << "" << "   ";
        output << std::setfill(' ');

        output << std::endl;

    }


    // -----------------------------------------------------------------------------------------
    // ----------------------- STRINGIFICATION :: Stringify Object Body ------------------------
    // -----------------------------------------------------------------------------------------
    void stringify_body(std::stringstream& output) {

        output << std::setw(11) << fid      << "   ";
        output << std::setw(9)  << refining << "   ";

        output << std::endl;

    }


    // -----------------------------------------------------------------------------------------
    // -------------------------- STRINGIFICATION :: Stringify Object --------------------------
    // -----------------------------------------------------------------------------------------
    std::string stringify() {

        std::stringstream output;

        stringify_head(output);
        stringify_body(output);

        return output.str();

    }

};


#endif // CSCI370FINALPROJECT_REFINERY_HPP
