// -------------------------------------------------------------------------------------------------
// ----------------------------------- HEADER :: Asteroid Object -----------------------------------
// -------------------------------------------------------------------------------------------------
#ifndef CSCI370FINALPROJECT_ASTEROID_HPP
#define CSCI370FINALPROJECT_ASTEROID_HPP

#include <iomanip>
#include <sstream>

#include <occi.h>
using namespace oracle::occi;


// -------------------------------------------------------------------------------------------------
// ----------------------------------- STRUCT :: Asteroid Object -----------------------------------
// -------------------------------------------------------------------------------------------------
struct Asteroid {

    // -----------------------------------------------------------------------------------------
    // ------------------------------- ATTRIBUTES :: Attributes --------------------------------
    // -----------------------------------------------------------------------------------------
    std::string miningFor;
    std::string fid;


    // -----------------------------------------------------------------------------------------
    // ------------------------------ CONSTRUCTOR :: Constructor -------------------------------
    // -----------------------------------------------------------------------------------------
    Asteroid(ResultSet* data) {

        miningFor = !data->isNull(2) ? data->getString(2) : "";
        fid = data->getString(1);

    }


    // -----------------------------------------------------------------------------------------
    // ------------------------ STRINGIFICATION :: Stringify Table Head ------------------------
    // -----------------------------------------------------------------------------------------
    static void stringify_head(std::stringstream& output) {

        output << std::endl;

        output << std::setw(11) << std::left << "Asteroid ID" << "   ";
        output << std::setw(10) << std::left << "Mining For"  << "   ";

        output << std::endl;

        output << std::setfill('-');
        output << std::setw(11) << "" << "   ";
        output << std::setw(10) << "" << "   ";
        output << std::setfill(' ');

        output << std::endl;

    }


    // -----------------------------------------------------------------------------------------
    // ----------------------- STRINGIFICATION :: Stringify Object Body ------------------------
    // -----------------------------------------------------------------------------------------
    void stringify_body(std::stringstream& output) {

        output << std::setw(11) << fid       << "   ";
        output << std::setw(10) << miningFor << "   ";

        output << std::endl;

    }


    // -----------------------------------------------------------------------------------------
    // -------------------------- STRINGIFICATION :: Stringify Object --------------------------
    // -----------------------------------------------------------------------------------------
    std::string stringify() {

        std::stringstream output;

        stringify_head(output);
        stringify_body(output);

        return output.str();

    }

};


#endif // CSCI370FINALPROJECT_ASTEROID_HPP
